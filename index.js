function imgCarMotion(image) {
  // Create HTML elements needed for each carousel image
  const figure = document.createElement('figure')
  const video = document.createElement('video')
  const source = document.createElement('source')
  const figcaption = document.createElement('figcaption')
  // Append newly created elements to the DOM
  const motionPort = document.querySelector('#motionPort')
  motionPort.append(figure)
  figure.append(video)
  video.append(source)
  video.append('Your browser is not supported!,')
  figure.append(figcaption)
  //Set content and attributes
  video.setAttribute('class', 'portfolio__video')
  video.setAttribute('controls', true)
  video.setAttribute('muted', true)
  video.setAttribute('loop', true)
  source.setAttribute('src', image.pic)
  source.setAttribute('type', image.type)
  // add the caption to figcaption
  figcaption.innerHTML = image.caption
}
images.motion.forEach((image) => imgCarMotion(image))
